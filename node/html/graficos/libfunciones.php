<?

$esquinas = array();

/* Funciones utilizadas */
function CargaEsquinas($xsi, $ysi, $xii, $yii, $xid, $yid, $xsd, $ysd){
	$esquinas[0] = $xsi;
	$esquinas[1] = $ysi;
	$esquinas[2] = $xii;
	$esquinas[3] = $yii;
	$esquinas[4] = $xid;
	$esquinas[5] = $yid;
	$esquinas[6] = $xsd;
	$esquinas[7] = $ysd;
	return $esquinas;
	}

?>